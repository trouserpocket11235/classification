    def startNDVI(self):
        QApplication.setOverrideCursor(Qt.WaitCursor)
        #NIR
        uri = self.dlg.fileNIR.filePath()
        #Red
        uri2 = self.dlg.fileRed.filePath()

        #define short versions of the layernames
